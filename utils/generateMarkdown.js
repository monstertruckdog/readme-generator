function renderLicenseBadge(data) {
  const getBadgeLink = renderLicenseLink(data);
  if (getBadgeLink === 'NA') {
    return 'NA'
  } else {
    return `![license](${getBadgeLink})`
  }
}

function renderLicenseLink(data) {
  let badgeUrl = '';
  const badgeNameSpaceCount = data.badge.split('');
  let spaceCount = 0;
  for (let i = 0; i < badgeNameSpaceCount.length; i++) {
    if (badgeNameSpaceCount[i] === ' ') {
      spaceCount++;
    }
  }
  if (data.badge === 'None / Not Applicable') {
    badgeUrl = 'NA'
    return badgeUrl
  } else if (spaceCount > 0 && data.badge != 'None / Not Applicable') {
    const badgeUrlEncoding = data.badge.replace(/\s/g, '%20')
    badgeUrl = `https://img.shields.io/badge/license-${badgeUrlEncoding}-informational`
    return badgeUrl
  } else {
    badgeUrl = `https://img.shields.io/badge/license-${data.badge}-informational`
    return badgeUrl
  }

}

function renderLicenseSection(data) {
  if (data.badge) {
    renderLicenseBadge(data)
    return `### License
    ${badgeConstruct}\n\n`
  }
}

